#Perímetro y área de un rectángulo (o cuadrado)
largo = float ( input ( ' Ingrese el valor del largo  ' ) )
ancho = float ( input ( ' Escriba el valor del ancho  ' ) )

perimetro = 2 * largo + 2 * ancho
area = largo * ancho

print ( ' El perímetro es ', perimetro , 'cm y su área es ' , area ,  ' cm cuadrados ')